package avaliacaoFinal2;

public class Biblioteca {

	private String nome;
	private int capacidadeLivros;
	private String localizacao;
	private int livros;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getCapacidadeLivros() {
		return capacidadeLivros;
	}
	public void setCapacidadeLivros(int capacidadeLivros) {
		this.capacidadeLivros = capacidadeLivros;
	}
	public String getLocalizacao() {
		return localizacao;
	}
	public void setLocalizacao(String localizacao) {
		this.localizacao = localizacao;
	}
	public int getLivros() {
		return livros;
	}
	public void setLivros(int livros) {
		this.livros = livros;
	}
	
	

}
