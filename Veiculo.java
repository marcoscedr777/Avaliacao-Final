package avaliacaoFinal;

public class Veiculo {

		private String placa;
		private String cor;
		private int nPassageiros;
		private float capacidadeTanque;
		private float velocidadeMaxima;
		private float velocidadeAtual;
		private float consumoMedio;
		public String getPlaca() {
			return placa;
		}
		public void setPlaca(String placa) {
			this.placa = placa;
		}
		public String getCor() {
			return cor;
		}
		public void setCor(String cor) {
			this.cor = cor;
		}
		public int getnPassageiros() {
			return nPassageiros;
		}
		public void setnPassageiros(int nPassageiros) {
			this.nPassageiros = nPassageiros;
		}
		public float getCapacidadeTanque() {
			return capacidadeTanque;
		}
		public void setCapacidadeTanque(float capacidadeTanque) {
			this.capacidadeTanque = capacidadeTanque;
		}
		public float getVelocidadeMaxima() {
			return velocidadeMaxima;
		}
		public void setVelocidadeMaxima(float velocidadeMaxima) {
			this.velocidadeMaxima = velocidadeMaxima;
		}
		public float getVelocidadeAtual() {
			return velocidadeAtual;
		}
		public void setVelocidadeAtual(float velocidadeAtual) {
			this.velocidadeAtual = velocidadeAtual;
		}
		public float getConsumoMedio() {
			return consumoMedio;
		}
		public void setConsumoMedio(float consumoMedio) {
			this.consumoMedio = consumoMedio;
		}
		
		
	

}
